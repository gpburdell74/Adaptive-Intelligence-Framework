namespace Adaptive.Intelligence.BlazorBasic;

/// <summary>
/// Lists the types of operational / functional operators that are supported.
/// </summary>
public enum BlazorBasicOperationalOperators
{
    /// <summary>
    /// Indicates an unknown operator.
    /// </summary>
    Unknown = 0,
    /// <summary>
    /// Indicates the increment operator (++).
    /// </summary>
    Increment,
    /// <summary>
    /// Indicates the decrement operator (--).
    /// </summary>
    Decrement
}
