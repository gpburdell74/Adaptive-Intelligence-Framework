namespace Adaptive.Intelligence.BlazorBasic;

/// <summary>
/// Lists the types of arithmetic operators that are supported.
/// </summary>
public enum BlazorBasicMathOperators
{
    /// <summary>
    /// Indicates an unknown operator.
    /// </summary>
    Unknown = 0,
    /// <summary>
    /// Indicates the addition operator (+)
    /// </summary>
    Add,
    /// <summary>
    /// Indicates the subtraction operator (-)
    /// </summary>
    Subtract,
    /// <summary>
    /// Indicates the multiplication operator (*)
    /// </summary>
    Multiply,
    /// <summary>
    /// Indicates the division operator (/)
    /// </summary>
    Divide,
    /// <summary>
    /// Indicates the Exponent operator (^)
    /// </summary>
    Exponent,
    /// <summary>
    /// Indicates the modulus operator (%)
    /// </summary>
    Modulus
}
