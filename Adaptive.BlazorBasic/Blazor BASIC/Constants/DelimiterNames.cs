namespace Adaptive.BlazorBasic;

/// <summary>
/// Contains the constants definitions for the text delimiters to use.
/// </summary>
internal static class DelimiterNames
{
    /// <summary>
    /// The space character.
    /// </summary>
    public const string DelimiterSpace = " ";
    /// <summary>
    /// The CR character.
    /// </summary>
    public const string DelimiterCr = "\r";
    /// <summary>
    /// The LF character.
    /// </summary>
    public const string DelimiterLf = "\n";
    /// <summary>
    /// The ` character.
    /// </summary>
    public const string DelimiterChar = "`";
    /// <summary>
    /// The double-quote character.
    /// </summary>
    public const string DelimiterString = "\"";
    /// <summary>
    /// The open parenthesis character.
    /// </summary>
    public const string DelimiterOpenParens = "(";
    /// <summary>
    /// The close parenthesis character.
    /// </summary>
    public const string DelimiterCloseParens = ")";
    /// <summary>
    /// The open square bracket character.
    /// </summary>
    public const string DelimiterOpenBracket = "[";
    /// <summary>
    /// The close square bracket character.
    /// </summary>
    public const string DelimiterCloseBracket = "]";
    /// <summary>
    /// The open curly bracket character.
    /// </summary>
    public const string DelimiterOpenBlockBracket = "{";
    /// <summary>
    /// The close curly bracket  character.
    /// </summary>
    public const string DelimiterCloseBlockBracket = "}";
    /// <summary>
    /// The comma character.
    /// </summary>
    public const string DelimiterListSeparator = ",";
}
