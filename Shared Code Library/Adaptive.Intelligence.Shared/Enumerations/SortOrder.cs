namespace Adaptive.Intelligence.Shared
{
	/// <summary>
	/// Provides a list of sort ordering options.
	/// </summary>
	public enum SortOrder
	{
		/// <summary>
		/// Indicates no sorting.
		/// </summary>
		NoSort = 0,
		/// <summary>
		/// Indicates a sort in ascending order.
		/// </summary>
		Ascending,
		/// <summary>
		/// Indicates a sort in descending order.
		/// </summary>
		Descending,
		/// <summary>
		/// Indicates a sort in a custom order / definition.
		/// </summary>
		Custom
	}
}
