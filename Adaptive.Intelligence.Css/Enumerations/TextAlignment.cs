namespace Adaptive.Intelligence.Css
{
	/// <summary>
	/// Lists the types of horizontal text alignment that are currently supported.
	/// </summary>
	public enum TextAlignment
	{
		/// <summary>
		/// The not specifed value.
		/// </summary>
		NotSpecified = 0,
		/// <summary>
		/// The none value.
		/// </summary>
		None = 1,
		/// <summary>
		/// Indicates the text is aligned to the left.
		/// </summary>
		Left = 2,
		/// <summary>
		/// Indicates the text is aligned to the center.
		/// </summary>
		Center = 3,
		/// <summary>
		/// Indicates the text is aligned to the right.
		/// </summary>
		Right = 4,
		/// <summary>
		/// Indicates the text is justified.
		/// </summary>
		Justify = 5
	}
}
