		/// </summary>
		/// <param name="propertyName">
		/// A string containing the name of the property.
		/// </param>
		/// <returns>
		/// A string containing the formatted result like: "width: "; or <b>null</b>.
		/// </returns>
		public static string? FormatPropertyName(string? propertyName)
        {
            if (string.IsNullOrEmpty(propertyName))
                return null;
            else
                return propertyName + CssLiterals.CssSeparator + CssLiterals.Space;
