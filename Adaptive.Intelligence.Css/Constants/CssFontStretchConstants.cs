namespace Adaptive.Intelligence.Css
{
    /// <summary>
    /// Provides the CSS font-stretch value constants for the application.
    /// </summary>
    public static class CssFontStretchConstants
    {
        /// <summary>
        /// Makes the text as narrow as it gets.
        /// </summary>
        public const string UltraCondensed = "ultra-condensed";
        /// <summary>
        /// Makes the text narrower than condensed, but not as narrow as ultra-condensed.
        /// </summary>
        public const string ExtraCondensed = "extra-condensed";
        /// <summary>
        /// Makes the text narrower than semi-condensed, but not as narrow as extra-condensed.
        /// </summary>
        public const string Condensed = "condensed";
        /// <summary>
        /// Makes the text narrower than normal, but not as narrow as condensed.
        /// </summary>
        public const string SemiCondensed = "semi-condensed";
        /// <summary>
        /// Default value.No font stretching.
        /// </summary>
        public const string Normal = "normal";
        /// <summary>
        ///  Makes the text wider than normal, but not as wide as expanded
        /// </summary>
        public const string SemiExpanded = "semi-expanded";
        /// <summary>
        /// Makes the text wider than semi-expanded, but not as wide as extra-expanded
        /// </summary>
        public const string Expanded = "expanded";
        /// <summary>
        ///  Makes the text wider than expanded, but not as wide as ultra-expanded
        /// </summary>
        public const string ExtraExpanded = "extra-expanded";
        /// <summary>
        /// Makes the text as wide as it gets.
        /// </summary>
        public const string UltraExpanded = "ultra-expanded";
        /// <summary>
        /// Sets this property to its default value.
        /// </summary>
        public const string Initial = "initial";
        /// <summary>
        /// Inherits this property from its parent element.
        /// </summary>
        public const string Inherit = "inherit";
    }
}